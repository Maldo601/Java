package H_Bucles;
public class L_ComboNumTres {
    public static void main (String[] args) {
        for (int n=1; n<=2; n++){
            for (int m=0; m<=2; m++){
                for(int i=0; i<=2; i++){
                    System.out.println(n+""+m+""+i);
                }
            }
        }
    }
}
