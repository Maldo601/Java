package H_Bucles;
public class I_ComboNum {
    public static void main (String[] args) {
        // Anidaci√≥ de bucle
        for (int n=1; n<=9; n++){
            for (int m=0; m<10; m++){
                System.out.println(n + "" + m);
            }
        }
        // Bucle Normal per fer totes les combinacions de dos xifres:
        for (int j=10; j<100; j++)       
          System.out.print(j + " ");  
    }
}
